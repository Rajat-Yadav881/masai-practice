package com.one;
import java.util.*;
public class One {
	public static void main(String[] args) {
		int x = 5;
		float y = 3.14f;
		long z = 6000;
		
		
		//autoboxing
		Integer intobj = x;
		Float floatObj = y;
		Long longObj = z;
		
		System.out.println(intobj);
		System.out.println(floatObj);
		System.out.println(longObj);
	}
}
