package com.one;

public class Two {
	
	
	
	public static void main(String[] args) {
		//Integer --- int
		
		int a = 34;
		float v = 45.67f;
		long l = 345666;
		
		
		//autoboxing
		
		Integer a1 = a;
		Float f1 = v;
		Long l1 = l;
		
		System.out.println(a1);
		System.out.println(f1);
		System.out.println(l1);
	}
}
